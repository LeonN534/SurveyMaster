    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String consultaSQL = "INSERT INTO respuestas (encuestaID, numeroPregunta, texto_respuesta) VALUES (?, ?, ?)";
        
        try (
            // Establecer la conexi√≥n con la base de datos
            Connection conexion = DriverManager.getConnection(URL, USERNAME, PASSWORD);

            // Crear el PreparedStatement con la consulta SQL
            PreparedStatement declaracion = conexion.prepareStatement(consultaSQL)
        ) {
            // Establecer los valores de los par√°metros
            boolean algunaInsercionFallida = false;
            for(PanelPreguntaRespuesta respuesta : listaPanelesPregunta) {
                declaracion.setInt(1, Integer.parseInt(this.idencuesta));
                declaracion.setInt(2, respuesta.numeroPregunta); 
                if(respuesta.tipo_respuesta.equals("Texto")) {
                    declaracion.setString(3, respuesta.campoRespuesta.getText());  
                } else {
                    for(JRadioButton opc : respuesta.opcionesArray) {
                        if(opc.isSelected()) {
                            declaracion.setString(3, opc.getText());
                            break;
                        }
                    }
                }
              
                // Ejecutar la consulta
                int filasAfectadas = declaracion.executeUpdate();
                if (filasAfectadas <= 0) {
                    // Fallo al insertar en la segunda tabla
                    algunaInsercionFallida = true;
                    break;  // Salir del bucle
                }
            }
            if (algunaInsercionFallida) {
                // L√≥gica para manejar inserciones fallidas en la segunda tabla
                MensajeDialog preguntasNoCreadas = new MensajeDialog(this, true, "No se pudieron enviar las respuestas");
                preguntasNoCreadas.setLocationRelativeTo(null);
                preguntasNoCreadas.setVisible(true);
                this.dispose();
                SeleccionPrograma ventanaInicial = new SeleccionPrograma();
                ventanaInicial.setLocationRelativeTo(null);
                ventanaInicial.setVisible(true);
            } else {
                // √âxito al insertar en ambas tablas
                MensajeDialog formularioCreado = new MensajeDialog(this, true, "Las respuestas se enviaron con √©xito");
                formularioCreado.setLocationRelativeTo(null);
                formularioCreado.setVisible(true);
                this.dispose();
                SeleccionPrograma ventanaInicial = new SeleccionPrograma();
                ventanaInicial.setLocationRelativeTo(null);
                ventanaInicial.setVisible(true);
            }
            
            
        } catch (SQLException e) {
            // Manejar cualquier excepci√≥n de SQL
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

